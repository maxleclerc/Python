seconds_per_hour = 60 * 60
print("sec dans une heure :", seconds_per_hour)

seconds_per_day = seconds_per_hour * 24
print("sec dans un jour :", seconds_per_day)

print("division avec virgule :", seconds_per_day / seconds_per_hour)

print("division sans virgule :", seconds_per_day // seconds_per_hour)
